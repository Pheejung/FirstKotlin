package com.example.firstkotlin;

public class PersonJava {
    private  int age;
    private String name;

    public PersonJava(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setName(String name) {
        this.name = name;
    }
}
